/*Задание 1: Вывести из таблицы trip информацию о командировках тех сотрудников, фамилия которых заканчивается на букву «а»,
в отсортированном по убыванию даты последнего дня командировки виде. В результат включить столбцы name, city, per_diem, date_first, date_last.*/
--Запрос:
  SELECT 
    name,
    city,
    per_diem,
    date_first, 
    date_last
  FROM trip
  WHERE name LIKE '%а %'
  ORDER BY date_last DESC
  ;

--Задание 2: Вывести в алфавитном порядке фамилии и инициалы тех сотрудников, которые были в командировке в Москве.
--Запрос:
  SELECT name
  FROM trip
  WHERE city = 'Москва'
  GROUP BY name
  ORDER BY name
  ;

/*Задание 3: Для каждого города посчитать, сколько раз сотрудники в нем были. 
